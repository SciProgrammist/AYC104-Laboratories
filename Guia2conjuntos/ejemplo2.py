#Definiendo conjuntos: 
A =  {1,2,3,4,5}
B =  {4,5,6,7,8,9}
#union de conjuntos
print("Union de conjuntos A y B es ", A.union(B))
#interseccion de conjuntos: 
print("Interseccion de conjuntos: ", A.intersection(B))
#diferencia de conjuntos:
print("Diferencia de conjuntos A - B = ", A-B)
